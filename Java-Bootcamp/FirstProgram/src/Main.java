// Public: class can be accessed from anywhere
// Class: Named group of properties and functions
// File name must be the same as public class [name here]
// pvsm is the STARTPOINT
// static: run main(String[] args) from public class Main WITHOUT creating an object of class Main(or whatever the file name is)
// void: do not return anything
//String[] args: An array. If we put our println as args[0] it will print out what we type in command line
public class Main {

    public static void main(String[] args) {
        System.out.println("Hello World!");
    }
}